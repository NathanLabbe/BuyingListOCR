package com.example.buyinglistocr.model;

public class Correspondence {

    public int id;
    public String name;

    public Correspondence(int id, String name) {

        this.id = id;
        this.name = name;

    }

    public int getId() {

        return id;

    }

    public void setId(int id) {

        this.id = id;

    }

    public String getName() {

        return name;

    }

    public void setName(String name) {

        this.name = name;

    }

}
